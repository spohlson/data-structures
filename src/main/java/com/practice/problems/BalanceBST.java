package com.practice.problems;

public class BalanceBST {

	/**
	 * Balanced BST - Height & Weight are balanced.
	 * 
	 * Height-balanced: for any 2 leaves, the difference of the depth is at most
	 * 1.
	 * 
	 * Weight-balanced: for each node it holds that the number of inner nodes in
	 * the left subtree and the number of inner nodes in the right subtree
	 * differ by at most 1.
	 */

	/**
	 * Given a BST that may be unbalanced, convert it into a balanced BST that
	 * has minimum possible height.
	 * 
	 * Example 1:
	 * 
	 * Input:
	 *        30
	 *       /
	 *      20
	 *     /
	 *    10
	 *
	 * Output:
	 * 		20
	 * 	   /  \
	 *    10   30
	 * 
	 * 
	 * Example 2:
	 * 
	 * Input:
	 * 				4
	 * 			  /  \
	 * 			3	  5
	 * 		   /       \
	 * 		  2         6
	 * 		 /           \
	 * 	    1			  7
	 * 
	 * Output:
	 * 
	 * 			4
	 * 		  /   \
	 * 		2		6
	 * 	   / \     / \
	 * 	  1   3   5   7
	 */

}
